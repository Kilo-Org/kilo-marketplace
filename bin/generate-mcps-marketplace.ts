#!/usr/bin/env npx tsx
/**
 * Generate marketplace.yaml from individual MCP directories.
 *
 * Usage: npx tsx bin/generate-mcps-marketplace.ts
 */

import * as fs from "fs";
import * as path from "path";
import { fileURLToPath } from "url";
import * as yaml from "yaml";
import { Document } from "yaml";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const mcpsDir = path.join(__dirname, "..", "mcps");

const categories = new Set([
  "business",
  "data",
  "development",
  "observability",
  "productivity",
  "search",
  "web-automation",
]);

function validateSuggestFor(value: unknown, mcpId: string): void {
  if (value === undefined) return;
  if (!value || typeof value !== "object" || Array.isArray(value)) {
    throw new Error(`${mcpId}: suggest_for must be an object`);
  }

  const suggestFor = value as Record<string, unknown>;
  const unknownKey = Object.keys(suggestFor).find(
    (key) => key !== "filename" && key !== "vscode_extension",
  );
  if (unknownKey) {
    throw new Error(`${mcpId}: suggest_for has unknown property "${unknownKey}"`);
  }

  const filenames = suggestFor.filename;
  const vscodeExtensions = suggestFor.vscode_extension;
  if (filenames === undefined && vscodeExtensions === undefined) {
    throw new Error(
      `${mcpId}: suggest_for must contain filename or vscode_extension`,
    );
  }

  if (
    filenames !== undefined &&
    (!Array.isArray(filenames) ||
      filenames.length === 0 ||
      !filenames.every(
        (filename) =>
          typeof filename === "string" &&
          /^\*\.[A-Za-z0-9]+(?:\.[A-Za-z0-9]+)*$/.test(filename),
      ))
  ) {
    throw new Error(
      `${mcpId}: suggest_for.filename must be a non-empty list of patterns like "*.ipynb"`,
    );
  }

  if (
    vscodeExtensions !== undefined &&
    (!Array.isArray(vscodeExtensions) ||
      vscodeExtensions.length === 0 ||
      !vscodeExtensions.every(
        (extensionId) =>
          typeof extensionId === "string" &&
          /^[A-Za-z0-9][A-Za-z0-9-]*\.[A-Za-z0-9][A-Za-z0-9-]*$/.test(
            extensionId,
          ),
      ))
  ) {
    throw new Error(
      `${mcpId}: suggest_for.vscode_extension must be a non-empty list of extension IDs like "ms-toolsai.jupyter"`,
    );
  }
}

const items = fs
  .readdirSync(mcpsDir, { withFileTypes: true })
  .filter((d) => d.isDirectory() && !d.name.startsWith("."))
  .map((dir) => {
    const content = fs.readFileSync(path.join(mcpsDir, dir.name, "MCP.yaml"), "utf-8");
    const mcp = yaml.parse(content);

    if (!categories.has(mcp.category)) {
      throw new Error(`${dir.name}/MCP.yaml: invalid category "${mcp.category}"`);
    }
    if (mcp.tags !== undefined) {
      throw new Error(`${dir.name}/MCP.yaml: use category instead of tags`);
    }
    validateSuggestFor(mcp.suggest_for, mcp.id || dir.name);

    const marketplaceMcp = {} as typeof mcp;
    for (const [key, value] of Object.entries(mcp)) {
      marketplaceMcp[key] = value;
      if (key === "category") {
        marketplaceMcp.tags = [value];
      }
    }

    console.log(`Added: ${mcp.name}`);
    return marketplaceMcp;
  })
  .sort((a, b) => a.id.localeCompare(b.id));

const categoryCounts = new Map<string, number>();
for (const item of items) {
  categoryCounts.set(item.category, (categoryCounts.get(item.category) ?? 0) + 1);
}

const categorySummary = [
  "# MCP category usage",
  "#",
  `# ${items.length} MCPs across ${categoryCounts.size} categories. Each MCP has one primary category.`,
  "# This summary is generated by bin/generate-mcps-marketplace.ts; do not edit it manually.",
  "#",
  ...[...categoryCounts]
    .sort(([categoryA, countA], [categoryB, countB]) =>
      countB - countA || categoryA.localeCompare(categoryB),
    )
    .map(([category, count]) => `# - ${category}: ${count}`),
  "#",
].join("\n");

const doc = new Document({ items });
const output = `${categorySummary}\n${doc.toString({ lineWidth: 120 })}`;

fs.writeFileSync(path.join(mcpsDir, "marketplace.yaml"), output);

console.log(`\nGenerated marketplace.yaml with ${items.length} MCPs`);
